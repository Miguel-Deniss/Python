# > maior 
# < menor
# >= maior ou igual
# <= menor ou igual
# == igual
# != diferente 

maior = 10 > 5
menor = 10 < 5
maior_igual = 10 >= 10
menor_igual = 10 <= 10
igual = 10 == 10
diferente = 10 != 10

print(f'Maior: {maior}')
print(f'Menor: {menor}')
print(f'Maior ou igual: {maior_igual}')
print(f'Menor ou igual: {menor_igual}')
print(f'Igual: {igual}')
print(f'Diferente: {diferente}')
